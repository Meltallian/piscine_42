#include <unistd.h>

int	main(int ac, char *av[])
{
	int	i;
	int	j;

	i = 0;
	j = 1;
	while (j < ac)
	{
		while (av[j][i] != '\0')
		{
			write(1, &av[j][i], 1);
			i++;
		}
		i = 0;
		write (1, "\n", 1);
		j++;
	}
	return (0);
}
